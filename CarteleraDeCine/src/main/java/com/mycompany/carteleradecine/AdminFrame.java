    private Pelicula buscarPeliculaGlobal(String titulo) {
        for (int i = 0; i < AppContext.listaPeliculas.tamanio(); i++) {
            Pelicula p = AppContext.listaPeliculas.get(i);
            if (p.getTitulo().equalsIgnoreCase(titulo.trim())) {
                return p;
            }
        }
        return null;
    }
    
    private void altaPelicula() {
        JPanel panel = new JPanel(new GridLayout(0, 2, 8, 8));
        JTextField tTitulo    = new JTextField();
        JTextField tDirector  = new JTextField();
        JTextField tProductor = new JTextField();
        JTextField tDuracion  = new JTextField();
        
        String[] arrClasif = {"AA", "A", "B", "B15", "C", "D"};
        JComboBox<String> cmbClasif = new JComboBox<>(arrClasif);
        
        String[] arrGeneros = {"Acción", "Comedia", "Drama", "Ciencia Ficción", "Terror", "Romance", "Fantasía", "Animación", "Documental"};
        JComboBox<String> cmbGenero = new JComboBox<>(arrGeneros);
        
        panel.add(new JLabel("Título:")); panel.add(tTitulo);
        panel.add(new JLabel("Director:")); panel.add(tDirector);
        panel.add(new JLabel("Productor:")); panel.add(tProductor);
        panel.add(new JLabel("Clasificación:")); panel.add(cmbClasif);
        panel.add(new JLabel("Duración (min):")); panel.add(tDuracion);
        panel.add(new JLabel("Género:")); panel.add(cmbGenero);
 
        int r = JOptionPane.showConfirmDialog(this, panel, "Alta de Película", JOptionPane.OK_CANCEL_OPTION);
        
        if (r != JOptionPane.OK_OPTION){
            return;
        }
        
        try {
            int dur = Integer.parseInt(tDuracion.getText().trim());
            
            // Extraemos lo que el usuario seleccionó en las listas
            String clasifSel = (String) cmbClasif.getSelectedItem();
            String generoSel = (String) cmbGenero.getSelectedItem();

            Pelicula p = new Pelicula(tTitulo.getText().trim(), tDirector.getText().trim(),
                tProductor.getText().trim(), clasifSel, dur, generoSel);
                
            AppContext.listaPeliculas.add(p);
            AppContext.pilaDeshacer.push(p);
            mostrar("Película agregada: " + p.getTitulo());
        } catch (Exception ex) {
            mostrar("Error: duración inválida.");
        }
    }
    
    private void deshacerAlta() {
        try {
            // Extraemos el último elemento de la pila
            Pelicula pDeshacer = AppContext.pilaDeshacer.pop();
            
            if (pDeshacer == null) {
                mostrar("La pila está vacía. No hay nada que deshacer.");
                return;
            }

            // Reconstruimos la lista omitiendo la película que acabamos de deshacer
            ListaPeliculas nuevaLista = new ListaPeliculas();
            for (int i = 0; i < AppContext.listaPeliculas.tamanio(); i++) {
                Pelicula actual = AppContext.listaPeliculas.get(i);
                if (!actual.getTitulo().equalsIgnoreCase(pDeshacer.getTitulo())) {
                    nuevaLista.add(actual);
                }
            }
            AppContext.listaPeliculas = nuevaLista;
            mostrar("Se deshizo el alta de la película:\n" + pDeshacer.getTitulo());
            
        } catch (Exception e) {
            mostrar("La pila está vacía o ocurrió un error al deshacer.");
        }
    }
 
    private void altaHorario() {
        String titulo = JOptionPane.showInputDialog(this, "Título de la película:");
        if (titulo == null) return;
        
        //buscador global del archivo java
        Pelicula p = buscarPeliculaGlobal(titulo);
        if (p == null) { mostrar("Película no encontrada."); return; }

        JPanel panel = new JPanel(new GridLayout(0, 2, 8, 8));
        JTextField tFecha = new JTextField("2026-04-10");
        JTextField tHora  = new JTextField("18:00"); 
        JTextField tSala  = new JTextField();
        panel.add(new JLabel("Fecha (YYYY-MM-DD):")); panel.add(tFecha);
        panel.add(new JLabel("Hora (HH:MM):")); panel.add(tHora);
        panel.add(new JLabel("Sala:")); panel.add(tSala);

        int r = JOptionPane.showConfirmDialog(this, panel, "Alta de Horario — " + p.getTitulo(), JOptionPane.OK_CANCEL_OPTION);
        if (r != JOptionPane.OK_OPTION){
            return;
        }
        try {
            int numeroSala = Integer.parseInt(tSala.getText().trim());
            if (numeroSala < 1 || numeroSala > 10) {
                mostrar("Error: Sala no existente. El cine solo tiene salas del 1 al 10.");
                return;
            }

            // ── REGLA 2: Validar Horario (22:00 a 07:00) ──
            LocalTime horaPelicula = LocalTime.parse(tHora.getText().trim());
            // Si la hora es igual o mayor a las 22:00, o es antes de las 07:00, lanzamos error
            if (!horaPelicula.isBefore(LocalTime.of(22, 0)) || horaPelicula.isBefore(LocalTime.of(7, 0))) {
                mostrar("Error: El cine no está disponible a esa hora (Horario de servicio: 07:00 a 21:59).");
                return;
            }
            int idAutomatico = (int)(Math.random() * 9000) + 1000;
            //Creamos el horario tomando la duración directa de la película
            Horario h = new Horario(
                idAutomatico, // ID autogenerado
                LocalDate.parse(tFecha.getText().trim()), 
                LocalTime.parse(tHora.getText().trim()),
                tSala.getText().trim(), 
                p.getDuracionMin() //Duración tomada en automático de la película
            );
            boolean ok = p.agregarHorario(h);
            mostrar(ok ? " Horario agregado a: " + p.getTitulo() + "\nFolio asignado: " + idAutomatico : "Empalme de horario detectado.");
        } catch (Exception ex) {
            mostrar("Formato inválido. Use YYYY-MM-DD y HH:MM");
        }
    }
 
    private void bajaPelicula() {
        String titulo = JOptionPane.showInputDialog(this, "Título de la película a eliminar:");
        if (titulo == null) return;
        ListaPeliculas nueva = new ListaPeliculas();
        boolean found = false;
        for (int i = 0; i < AppContext.listaPeliculas.tamanio(); i++) {
            Pelicula p = AppContext.listaPeliculas.get(i);
            if (p.getTitulo().equalsIgnoreCase(titulo.trim())) found = true;
            else nueva.add(p);
        }
        if (found) { AppContext.listaPeliculas = nueva; mostrar("Película eliminada: " + titulo); }
        else mostrar("Película no encontrada.");
    }
 
    private void bajaHorario() {
        String titulo = JOptionPane.showInputDialog(this, "Título de la película:");
        if (titulo == null) return;
        
        // ¡CAMBIO AQUÍ! Usamos el buscador global
        Pelicula p = buscarPeliculaGlobal(titulo);
        if (p == null) { mostrar("Película no encontrada."); return; }

        mostrarHorariosEnArea(p);

        String idStr = JOptionPane.showInputDialog(this, "ID del horario a eliminar:");
        if (idStr == null) return;
        try {
            boolean ok = p.eliminarHorario(Integer.parseInt(idStr.trim()));
            mostrar(ok ? "Horario eliminado." : "ID de horario no encontrado.");
        } catch (Exception ex) { mostrar("ID inválido."); }
    }
 
    private void modificarPelicula() {
        String titulo = JOptionPane.showInputDialog(this, "Título de la película a modificar:");
        if (titulo == null) return;
        
        // ¡CAMBIO AQUÍ! Usamos el buscador global
        Pelicula p = buscarPeliculaGlobal(titulo);
        if (p == null) { mostrar("Película no encontrada."); return; }

        JPanel panel = new JPanel(new GridLayout(0, 2, 8, 8));
        JTextField tTitulo = new JTextField(p.getTitulo());
        JTextField tDir    = new JTextField(p.getDirector());
        JTextField tProd   = new JTextField(p.getProductor());
        JTextField tClasif = new JTextField(p.getClasificacion());
        JTextField tGenero = new JTextField(p.getGenero());
        JTextField tDuracion = new JTextField(String.valueOf(p.getDuracionMin()));
        // Listas desplegables como en Alta
        String[] arrClasif = {"AA", "A", "B", "B15", "C", "D"};
        JComboBox<String> cmbClasif = new JComboBox<>(arrClasif);
        cmbClasif.setSelectedItem(p.getClasificacion()); // Seleccionamos la que ya tenía

        String[] arrGeneros = {"Acción", "Comedia", "Drama", "Ciencia Ficción", "Terror", "Romance", "Fantasía", "Animación", "Documental"};
        JComboBox<String> cmbGenero = new JComboBox<>(arrGeneros);
        cmbGenero.setSelectedItem(p.getGenero()); // Seleccionamos el que ya tenía
        
        panel.add(new JLabel("Título:")); panel.add(tTitulo);
        panel.add(new JLabel("Director:")); panel.add(tDir);
        panel.add(new JLabel("Productor:")); panel.add(tProd);
        panel.add(new JLabel("Clasificación:")); panel.add(cmbClasif);
        panel.add(new JLabel("Duración (min):")); panel.add(tDuracion);
        panel.add(new JLabel("Género:")); panel.add(cmbGenero);

        int r = JOptionPane.showConfirmDialog(this, panel, "Modificar — " + p.getTitulo(), JOptionPane.OK_CANCEL_OPTION);
        if (r != JOptionPane.OK_OPTION) return;
        try {
            int dur = Integer.parseInt(tDuracion.getText().trim());
            
            p.setTitulo(tTitulo.getText().trim());
            p.setDirector(tDir.getText().trim());
            p.setProductor(tProd.getText().trim());
            p.setClasificacion((String) cmbClasif.getSelectedItem());
            p.setGenero((String) cmbGenero.getSelectedItem());
            p.setDuracionMin(dur); // Modificamos la duración
            
            mostrar("✅ Película modificada con éxito:\n" + p.getTitulo());
        } catch (Exception ex) {
            mostrar("❌ Error: La duración debe ser un número entero.");
        }
    }
 
    private void consultarPelicula() {
        String titulo = JOptionPane.showInputDialog(this, "Título a consultar:");
        if (titulo == null) return;
        Pelicula p = buscarPeliculaGlobal(titulo);
        if (p == null) { mostrar("Película no encontrada."); return; }
        
        StringBuilder sb = new StringBuilder();
        sb.append("══ ").append(p.getTitulo()).append(" ══\n");
        sb.append("Director:      ").append(p.getDirector()).append("\n");
        sb.append("Productor:     ").append(p.getProductor()).append("\n");
        sb.append("Clasificación: ").append(p.getClasificacion()).append("\n");
        sb.append("Género:        ").append(p.getGenero()).append("\n");
        sb.append("Duración:      ").append(p.getDuracionMin()).append(" min\n");
        mostrar(sb.toString());
        mostrarHorariosEnArea(p);
    }
 
    private void consultarCartelera() {
        if (AppContext.listaPeliculas.tamanio() == 0) { mostrar("No hay películas en cartelera."); return; }
        StringBuilder sb = new StringBuilder("══ CARTELERA ══\n");
        sb.append("Estado: ").append(AppContext.nombreEstado).append(" | Municipio: ").append(AppContext.nombreMunicipio).append("\n\n");
        for (int i = 0; i < AppContext.listaPeliculas.tamanio(); i++) {
            Pelicula p = AppContext.listaPeliculas.get(i);
            sb.append(i + 1).append(". ").append(p.getTitulo())
              .append("  [").append(p.getClasificacion()).append("]  ").append(p.getGenero()).append("\n");
        }
        mostrar(sb.toString());
    }
 
    private void mostrar(String texto) {
        areaResultados.setText(texto);
    }
 
    private void mostrarHorariosEnArea(Pelicula p) {
        StringBuilder sb = new StringBuilder(areaResultados.getText()).append("\n\n-- Horarios --\n");
        
        // Asumiendo que tu clase Pelicula tiene un método getHorarios() que devuelve la ListaHorarios
        if (p.getHorarios() == null || p.getHorarios().tamanio() == 0) {
            sb.append("No hay horarios registrados.");
        } else {
            for (int i = 0; i < p.getHorarios().tamanio(); i++) {
                Horario h = p.getHorarios().get(i);
                sb.append("Folio: ").append(h.getId())
                  .append(" | Sala: ").append(h.getSala())
                  .append(" | Fecha: ").append(h.getFecha())
                  .append(" | Hora: ").append(h.getHoraInicio()).append("\n");
            }
        }
        areaResultados.setText(sb.toString());
    }
 
